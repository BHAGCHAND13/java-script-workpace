const value = 3;
switch (value) {
    case 1:
        console.log("jan");
        break;
    case 2:
        console.log("feb");
        break;
    case 3:
        console.log("march");
        break;
    case 4:
        console.log("april");
        break;
    case 5:
        console.log("jun");
        break;
    default:
        console.log("case is not match");
        break;
}